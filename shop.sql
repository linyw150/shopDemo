DROP TABLE IF EXISTS `t_shop`;

CREATE TABLE `t_shop` (
`id` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '主键',
`manager_id` INT(10) UNSIGNED NOT NULL DEFAULT '0' COMMENT '店长ID',
`shopname` VARCHAR(20) NOT NULL DEFAULT '' COMMENT '店铺名称',
`shoplogo` VARCHAR(255) NOT NULL DEFAULT '' COMMENT '店铺LOGO',
`shopdescription` VARCHAR(255) NOT NULL DEFAULT '' COMMENT '店铺简介',
`shopaddress` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '店铺地址',
`shoplongitude` DECIMAL(10,6) NOT NULL DEFAULT '0.000000' COMMENT '店铺经度',
`shoplatitude` DECIMAL(10,6) NOT NULL DEFAULT '0.000000' COMMENT '店铺纬度',
`createtime` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '记录创建时间',
`updatetime` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '最近更新时间',
  PRIMARY KEY (`id`)
)ENGINE=MYISAM  DEFAULT CHARSET=utf8;


DROP TABLE IF EXISTS `t_manager`;

